namespace wj.AgnosticSql.Sql
{
    /// <summary>
    /// Defines the possible sorting directions in an RDBMS.
    /// </summary>
    public enum SortDirections
    {
        /// <summary>
        /// Ascending order (from small to large).
        /// </summary>
        Ascending = 0,
        /// <summary>
        /// Descending order (from large to small).
        /// </summary>
        Descending = 1
    }
}